const fs = require('fs')
const path = require('path')

function parseEnv(content) {
  const out = {}
  const lines = content.split(/\r?\n/)

  for (const rawLine of lines) {
    const line = rawLine.trim()
    if (!line || line.startsWith('#')) continue

    const eqIndex = line.indexOf('=')
    if (eqIndex <= 0) continue

    const key = line.slice(0, eqIndex).trim()
    let value = line.slice(eqIndex + 1).trim()

    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }

    out[key] = value
  }

  return out
}

function main() {
  const appRoot = path.resolve(__dirname, '..')
  const envPath = path.join(appRoot, '.env')
  const targetPath = path.join(appRoot, 'config', 'env.generated.js')

  if (!fs.existsSync(envPath)) {
    console.error('Missing .env at', envPath)
    process.exit(1)
  }

  const envText = fs.readFileSync(envPath, 'utf8')
  const env = parseEnv(envText)

  const file = [
    '// Auto-generated by scripts/sync-env-to-config.js',
    '// Do not edit manually. Update LuffaApp/.env and re-run the script.',
    '',
    'module.exports = ' + JSON.stringify(env, null, 2),
    ''
  ].join('\n')

  fs.writeFileSync(targetPath, file, 'utf8')
  console.log('Generated', targetPath)
}

main()
